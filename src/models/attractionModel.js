import mongoose from "mongoose";

const attractionSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    startTime: {
      type: String,
      required: true,
    },
    endTime: {
      type: String,
      required: true,
    },
  },
);

const Attraction = mongoose.model("Attraction", attractionSchema);

export default Attraction;